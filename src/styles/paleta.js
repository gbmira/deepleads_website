const cores = type => {
  if( type === 'first' ) {
    return '#2171AC'
  }

  if( type === 'second' ) {
    return '#4B4B4B'
  }

  if( type === 'third' ) {
    return '#FFFBFE'
  }

  if( type === 'fourth' ) {
    return '#25804F'
  }
  
  if( type === 'fifth' ) {
    return '#333'
  }
  
  if( type === 'branco' ) {
    return '#fff'
  }
}

export default cores;